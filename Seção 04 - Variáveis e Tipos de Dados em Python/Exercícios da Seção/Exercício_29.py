"""
29) Leia quatro notas, calcule a média aritmética e imprima o resultado.
"""

note_1 = float(input('Enter note one: '))
note_2 = float(input('Enter note two: '))
note_3 = float(input('Enter note three: '))
note_4 = float(input('Enter note four: '))
soma = (note_1 + note_2 + note_3 + note_4) / 4
print(f'The arithmetic mean of grades is: {soma}')
